    private void select_coverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_select_coverActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Select Book Cover");
        
        fileChooser.setCurrentDirectory(new File("download"));
        
        FileNameExtensionFilter extensionFilter = new FileNameExtensionFilter("Image", ".png", ".jpg", ".jpeg");
        fileChooser.addChoosableFileFilter(extensionFilter);
        
        int fileState = fileChooser.showSaveDialog(null);
        
        if(fileState == JFileChooser.APPROVE_OPTION){
            String path = fileChooser.getSelectedFile().getAbsolutePath();
            label_imagePath.setText(path);
            imagePath = path;
        }
    }//GEN-LAST:event_select_coverActionPerformed

