    //setting the book detail to table
    public void setBookDetailsToTable(){
        
        try{
            Connection conn = database.getConnection();
            String query = "SELECT * FROM book_data";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(query);
            
            while (rs.next()){
                String bookid = rs.getString("book_id");
                String booktitle = rs.getString("book_title");
                String bookcat = rs.getString("book_category");
                String bookauthor = rs.getString("book_author");
                String booksyn = rs.getString("book_synopsis");
                byte[] bookcover = rs.getBytes("book_photo");
                
                
                Object[] obj = {bookid, booktitle, bookcat, bookauthor, booksyn, bookcover};
                model = (DefaultTableModel)book_table.getModel();
                model.addRow(obj);
                book_table.setShowVerticalLines(true);
                book_table.setShowHorizontalLines(true);
                book_table.getColumnModel().getColumn(5).setCellRenderer(new ImageRender());
                
            }
            
        }catch(Exception e){
            e.printStackTrace();
        }
        
        MultiLineJTable renderer = new MultiLineJTable();
        
        //set table cell renderer into a specified JTable column class
        book_table.setDefaultRenderer(String[].class, renderer);
        
    }
    

    
    

